function removeChars(str, arr) {
    let result = '';

    for (let i = 0; i < str.length; i++) {
        let char = str[i];
        let shouldSkip = false; // –§–ª–∞–≥, –∫–æ—Ç–æ—Ä—ã–π –≥–æ–≤–æ—Ä–∏—Ç, –Ω—É–∂–Ω–æ –ª–∏ –ø—Ä–æ–ø—É—Å–∫–∞—Ç—å —Å–∏–º–≤–æ–ª

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —Å–∏–º–≤–æ–ª –≤ –º–∞—Å—Å–∏–≤–µ arr
        for (let j = 0; j < arr.length; j++) {
            if (char === arr[j]) {
                shouldSkip = true; // –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª –Ω–∞–π–¥–µ–Ω, –æ—Ç–º–µ—á–∞–µ–º, —á—Ç–æ –µ–≥–æ –Ω—É–∂–Ω–æ –ø—Ä–æ–ø—É—Å—Ç–∏—Ç—å
                break; 
            }
        }

        if (shouldSkip) continue; // –ï—Å–ª–∏ —Å–∏–º–≤–æ–ª –Ω–∞–π–¥–µ–Ω –≤ arr, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –µ–≥–æ

        result += char;
    }

    return result;
}

