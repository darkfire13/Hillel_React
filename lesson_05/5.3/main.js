// Дано ціле число N (ввести через prompt). Вивести всі цілі числа від 1 до 100, квадрат яких не перевищує числа N

let num = +prompt("Введите число:");
// let num = prompt("Введите число:");

if (isNaN(num) || num <= 0) {
    console.log("Ошибка: Введите корректное положительное число!");
} else {
    for (let i = 1; i <= 100; i++) {
        let square = i * i;
        if (square <= num) {
            console.log(`${square} <= ${num}`);
        } else {
            break; // Если квадрат уже больше, дальше проверять нет смысла
        }
    }
}
